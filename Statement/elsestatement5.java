package Statement;

public class elsestatement5 {
    public static void main(String[] args) {

        int A =50;

        int B = 60;

        System.out.println(A+B); //A+B=50
        System.out.println(A-B); //A-B=-10
        System.out.println(A*B); //A*B=3000
        System.out.println(A/B); //A*B=0
        System.out.println(++A); //1+A=51
        System.out.println(A);   //Value now is 51 after adding 50+1
        System.out.println(--A); //So now 51-1
    }
}
